from __future__ import annotations

from aiogram import Router
from aiogram.filters import Command
from aiogram.types import Message


router = Router(name="help")


@router.message(Command("help"))
async def cmd_help(message: Message) -> None:
    help_text = """
**Демотиватор Бот 🎨**

**Демотиваторы:**
/d [текст] — создать демотиватор
/dd [текст] — то же самое
/д [текст] — русский вариант
/дд [текст] — русский вариант

**Эффекты (только для изображений):**
/inv [текст] — инверсия цветов
/vin [текст] — винтажная обработка

**Эмодзи-паки:**
/emoji — нарезать картинку/видео на эмодзи
/emoji 4x4 — сетка 4x4 (16 эмодзи)
/emoji 5x5 — сетка 5x5 (25 эмодзи)

**Другие команды:**
/tenet — переворот медиа (зеркало, реверс, антипод)
/trump — переписать текст в стиле Трампа
/aquastar, /зал — текущая загруженность AQUASTAR Павелецкая
/aquas, /aquas3, /aquas7, /aquas30 — история AQUASTAR за указанное число дней
/зал3, /зал7, /зал30 — то же самое

**Рейтинг и лычки:**
/profile — мой профиль (рейтинг + лычка)
/top — топ пользователей
/plus — +1 рейтинга (ответь на сообщение)
/title — синхронизировать лычку в титуле админа (ответом или себе)
/synctitles — синхронизировать титулы у всех админов
/award Мыслитель — выдать лычку (поднять рейтинг до порога) ответом на сообщение
/promote [invite|mod|admin] — выдать админку через бота (тогда бот сможет менять титул)
/seedrep 1 249 — раздать стартовый рейтинг админам (только тем, у кого рейтинг = 0)
"""
    await message.answer(help_text, parse_mode="Markdown")
